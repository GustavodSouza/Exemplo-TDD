package br.teste.tdd.Teste.TDD;

public class Calculadora {
	
	public Integer somar(int numero1, int numero2) {
		return (numero1 + numero2);
	}
	
	public Integer subtrair(int numero1, int numero2) {
		return (numero1 - numero2);
	}
	
	public Integer dividir(int numero1, int numero2) {
		return (numero1 / numero2);
	}
	
	public Integer multiplicar(int numero1, int numero2) {
		return (numero1 * numero2);
	}
	
	public Integer resto(int numero1, int numero2) {
		return (numero1 % numero2);
	}
}
